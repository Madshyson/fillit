#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*tmp;
	size_t			i;

	i = 0;
	tmp = s;
	while (n > 0)
	{
		tmp[i] = c;
		i++;
		n--;
	}
	return (s);
}
